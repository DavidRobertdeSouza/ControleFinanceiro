async function carregarEmpresas(){
    const urlEmpresas = 'https://api-cotacao-b3.labdo.it/api/empresa'
    return fetch(urlEmpresas)
    .then(async response => {
        return response.json().then(data => {
            let array = []
            for(let d of data){
                if(d.cd_acao.includes(',')){
                    let split = d.cd_acao.split(', ')
                    for(let i in split){
                        let objeto = {nome: d.nm_empresa, sigla:split[i]}
                        array.push(objeto)
                    }
                }
                else if(d.cd_acao){
                    let objeto = {nome: d.nm_empresa, sigla:d.cd_acao}
                    array.push(objeto)
                }
            }
            return array
        })
    }
    )
}



